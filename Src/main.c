void checkBPM(uint32_t adcResult)
{
		if(belowThreshold && adcResult>threshold) // wait untill new beat reaches threshold 
			{
				bpm ++;
				belowThreshold=0; //to only  count once
			} 
			else
			if(adcResult<threshold) //if fell below 2 then this is a new beat 
			{
				belowThreshold=1;
			}	
				 
}

void tickInit ()
{
	SystemCoreClockUpdate();
	SysTick_Config(SystemCoreClock/samplespersec) ;//1 msec delay between 2 successive interrupts
	
}


void minuteDone()
{
			sprintf(Buffer, "Count = %d\r\n", i);
		  HAL_UART_Transmit(&huart1,(uint8_t *)&Buffer,strlen(Buffer),10);
	    sprintf(Buffer, "BPM= %d\r\n",bpm);
		  HAL_UART_Transmit(&huart1,(uint8_t *)&Buffer,strlen(Buffer),10);
			stop = 1;
	    HAL_ADC_Stop (&hadc1);
			startSampling=0 ;
	    cnt =0 ;
}

void takeSample()
{

	
	ADC_Data= HAL_ADC_GetValue(&hadc1);
	
	sprintf(Buffer, "%d\r\n", ADC_Data);
	
  HAL_UART_Transmit(&huart1,(uint8_t *)&Buffer,strlen(Buffer),10);
	
	
	i++;
	
	 checkBPM(ADC_Data);
	
}


void HAL_IncTick(void)
{
	if (startSampling)
	{
		ticks++ ;
		
		
	
		  if (stop == 0) 
				takeSample(); 
			
			if ((ticks >= period*samplespersec) && (stop == 0)) //if a minute has passed
				minuteDone();
	}
		
}

void HAL_UART_RxCpltCallback (UART_HandleTypeDef * huart)
{
	//DARMSTM.DLL-pSTM32F103C8
	HAL_UART_Receive_IT (&huart1,(uint8_t *) s , sizeof(s));
	
	if (strcmp (s , "\r") == 0)
	{
		cnt++;
		if (cnt == 2)
		{
			stop = 0;
			i  =0 ;
			ticks = 0 ;
			belowThreshold=1;
			bpm =0 ;
			
			int c = -1 ;
			sscanf(ss, "%d", &c);
			
			if (c > 1000)
				samplespersec = 1000;
			else
				samplespersec = c ;
			
			startSampling =1 ;
			//samplingrate = 1000 / samplespersec ; // samplespersec ---> 1000msec then 1 smaple ---> ?
			
			tickInit ();
						
			HAL_ADC_Start(&hadc1);
			memset (ss , 0 , sizeof (ss));
	}
		else
			if (cnt == 1)
			{
					int c = -1 ;
			    sscanf(ss, "%d", &c);
				if (c > 0)
					period = c ;
				else
					c = 60 ;
			  	memset (ss , 0 , sizeof (ss));
			}

	}
	else
	{
		strcat (ss, s);
	}
	
}


